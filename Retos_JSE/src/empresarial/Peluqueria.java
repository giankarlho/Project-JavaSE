    private void cbocomboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbocomboActionPerformed
        String combo = (String) cbocombo.getSelectedItem();
        cbntip.removeAllItems();
        if (combo.equals("Corte")) {
            cbntip.addItem("Selecione");

            cbntip.addItem("corte de pelo mujer");
            cbntip.addItem("corte de pelo hombre");
            cbntip.addItem("corte de pelo infantil");
        }
        if (combo.equals("Peinado")) {
            cbntip.addItem("Selecione");
            cbntip.addItem("peinado");
            cbntip.addItem("peinado con rulos");
            cbntip.addItem("recogidos");
        }
        if (combo.equals("Tinte")) {
            cbntip.addItem("Selecione");
            cbntip.addItem("tinte o ba√±o de color");
            cbntip.addItem("retoque color zona");
            cbntip.addItem("matizados");
        }
        if (combo.equals("Mechas")) {
            cbntip.addItem("Selecione");
            cbntip.addItem("mechas");
            cbntip.addItem("decoloracion");

        }
        if (combo.equals("Moldeado")) {
            cbntip.addItem("Selecione");
            cbntip.addItem("moldeado");
            cbntip.addItem("desrizado");
        }
        if (combo.equals("Tratamiento")) {
            cbntip.addItem("Selecione");
            cbntip.addItem("ritual express");
            cbntip.addItem("ritual intensive");
        }

    }//GEN-LAST:event_cbocomboActionPerformed

